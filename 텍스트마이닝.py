"""
    단순한 텍스트 필터링이 아니라, 기사 속에 숨겨진 기자의 심리, 의도, 
    그리고 정서적 이면을 분석하는 
    '텍스트 마이닝(Text Mining) 및 프로파일링'
"""

word = """서울시청 주관으로 지역 활성화를 위하여 잔치국수를 
시식하는 행사를 진행하였습니다. 70세이상은 노인분들에게는 무료로 진행되며,
그외 노인분들은 단 돈 천원에 시식하실 수 있습니다.
거동이 불편하신 분들은 요양사들과 함께 참석이 가능합니다.
이날을 위해 준비한 행사는 그 밖에도 안마기계 체험도 있습니다.
협찬기업 바디프렌즈도 참석하여 함께 진행합니다.
70세 이하 60세 이상인 노인분들은 사회복지사와 참석하시면 무료로 시식하실 수 있습니다. 
"""

base_stop_words = {'노인', '참석', '일동', '주민', 
                   "지역", "마을", "노인들", "노인분들", "주민들","주민일동",
                   "이날" }

# 각 단어별 분석 결과를 담을 딕셔너리
profiling_result = {}

for target in base_stop_words:
    indices = []  # 이 단어가 발견된 모든 인덱스를 저장할 리스트
    start_index = 0  # 검색을 시작할 위치 (0번 글자부터 시작)
    
    while True:
        # start_index 이후부터 target 단어를 찾습니다.
        pos = word.find(target, start_index)
        
        
        # 더 이상 단어가 발견되지 않으면 (-1) 반복을 멈춥니다.
        if pos == -1:
            break
            
        indices.append(pos)  # 발견된 위치 기록
        start_index = pos + 1  # ⭐ 중요: 다음 검색은 발견된 위치 '바로 다음 칸'부터 시작!
        
    # 단어가 한 번이라도 등장했다면 결과에 기록합니다.
    if indices:
        profiling_result[target] = {
            "빈도수(Count)": len(indices),
            "등장위치(Indices)": indices
        }

# --- 보기 좋게 출력 ---
import json
print(json.dumps(profiling_result, ensure_ascii=False, indent=2))

